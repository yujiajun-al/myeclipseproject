package cn.yjj.pojo;

import java.util.Date;

public class Public_class {
	private Integer id; 
	private Date time; 
	private String name; 
	private String classes; 
	private String proof_materials;
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public Date getTime() {
		return time;
	}
	public void setTime(Date time) {
		this.time = time;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getClasses() {
		return classes;
	}
	public void setClasses(String classes) {
		this.classes = classes;
	}
	public String getProof_materials() {
		return proof_materials;
	}
	public void setProof_materials(String proof_materials) {
		this.proof_materials = proof_materials;
	}
	
}
